# Day 6 - PowerShell365
# Use the Get-Help core cmdlet to invoke
# a built-in help system.
# https://github.com/edoardottt/powershell365


# These examples display basic help information about the Format-Table cmdlet.
Get-Help Format-Table
Get-Help -Name Format-Table
Format-Table -?

# These examples display more detailed help information about the Format-Table cmdlet.
Get-Help Format-Table -Detailed
Get-Help Format-Table -Full

# https://docs.microsoft.com/en-us/powershell/module/microsoft.powershell.core/get-help?view=powershell-7.1
